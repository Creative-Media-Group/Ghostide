package lsp4custom.com.ninjacoder.customhtmllsp;

public class Materialize {

  private String name;
  private String id;

  public Materialize(String name, String id) {
    this.name = name;
    this.id = id;
  }

  public String getName() {
    return this.name;
  }

  

  public String getId() {
    return this.id;
  }

  
}
